package qwirkle;

/**
 * Represents the shape of a particular stone in the Qwirkle game.
 * @author Jerre
 *
 */
public enum StoneShape {
	/**
	 * Indicates the shape of the stone is a circle.
	 */
	CIRCLE,
	/**
	 * Indicates the shape of the stone is a cross.
	 */
	CROSS,
	/**
	 * Indicates the shape of the stone is a diamond.
	 */
	DIAMOND,
	/**
	 * Indicates the shape of the stone is a rectangle.
	 */
	RECTANGLE,
	/**
	 * Indicates the shape of the stone is a star.
	 */
	STAR,
	/**
	 * Indicates the shape of the stone is a clubs.
	 */
	CLUBS
}
