package qwirkle;

/**
 * Represents the color of a particular stone in the Qwirkle game.
 * @author Jerre
 *
 */
public enum StoneColor {
	/**
	 * Indicates the color of the stone is red.
	 */
	RED,
	/**
	 * Indicates the color of the stone is orange.
	 */
	ORANGE,
	/**
	 * Indicates the color of the stone is yellow.
	 */
	YELLOW,
	/**
	 * Indicates the color of the stone is green.
	 */
	GREEN,
	/**
	 * Indicates the color of the stone is blue.
	 */
	BLUE,
	/**
	 * Indicates the color of the stone is purple.
	 */
	PURPLE
}
