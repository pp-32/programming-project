package qwirkle.client;

/**
 * Provides values for possible player types.
 * @author Jerre
 *
 */
public enum PlayerType {
	/**
	 * Indicates the player is a human.
	 */
	HUMAN,
	
	/**
	 * Indicates the player is a computer using the naive strategy.
	 */
	NAIVE,
	
	/**
	 * Indicates the player is a computer using the smart strategy.
	 */
	SMART,
}
